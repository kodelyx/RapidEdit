# Batch intervals to avoid giant filter graph crash / slowness
BATCH_SIZE = 40
batches = [intervals[i:i + BATCH_SIZE] for i in range(0, len(intervals), BATCH_SIZE)]

temp_dir = Path("temp_parts")
temp_dir.mkdir(exist_ok=True)

def process_batch(batch_idx, batch_intervals):
    T_start = batch_intervals[0][0]
    T_end = batch_intervals[-1][1]
    T_seek_start = max(0.0, T_start - 5.0)

    filter_parts = []
    concat_inputs = []
    for local_idx, (start, end) in enumerate(batch_intervals):
        rel_start = start - T_seek_start
        rel_end = end - T_seek_start
        filter_parts.append(
            f"[0:v]trim=start={rel_start:.2f}:end={rel_end:.2f},setpts=PTS-STARTPTS[v{local_idx}];"
            f"[0:a]atrim=start={rel_start:.2f}:end={rel_end:.2f},asetpts=PTS-STARTPTS[a{local_idx}]"
        )
        concat_inputs.append(f"[v{local_idx}][a{local_idx}]")

    filter_complex = (
        ";".join(filter_parts)
        + ";"
        + "".join(concat_inputs)
        + f"concat=n={len(batch_intervals)}:v=1:a=1[cv][outa];[cv]fps=30[outv]"
    )

    temp_ts_file = temp_dir / f"part_{batch_idx:04d}.ts"
    cmd = [
        "ffmpeg", "-y",
        "-ss", f"{T_seek_start:.2f}",
        "-accurate_seek",
        "-hwaccel", "videotoolbox",
        "-i", str(input_file),
        "-t", f"{T_end - T_seek_start:.2f}",
        "-filter_complex", filter_complex,
        "-map", "[outv]", "-map", "[outa]",
        "-c:v", "h264_videotoolbox", "-b:v", "10M",
        "-pix_fmt", "yuv420p",
        "-c:a", "aac", "-b:a", "192k",
        str(temp_ts_file)
    ]

    print(f"🚀 Starting Batch {batch_idx + 1}/{len(batches)} (time: {T_start:.2f}s to {T_end:.2f}s)...")
    try:
        # Run with check=True to raise error on failure
        subprocess.run(cmd, check=True, stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL)
        print(f"✅ Finished Batch {batch_idx + 1}/{len(batches)}")
        return temp_ts_file
    except subprocess.CalledProcessError as e:
        print(f"❌ Error in Batch {batch_idx + 1} processing.")
        raise e

# Run batch processing sequentially to avoid macOS videotoolbox hardware session limits
ts_files = []
print(f"Processing {len(intervals)} intervals in {len(batches)} batches sequentially...")

for idx, batch in enumerate(batches):
    try:
        ts_file = process_batch(idx, batch)
        ts_files.append(ts_file)
    except Exception as e:
        print("Processing stopped due to batch execution failure.")
        raise e

# Create concat list file
concat_list_file = Path("concat_list.txt")
with open(concat_list_file, "w") as f:
    for ts_file in ts_files:
        if ts_file:
            f.write(f"file '{ts_file.resolve()}'\n")

# Final Concatenation
concat_cmd = [
    "ffmpeg", "-y",
    "-f", "concat",
    "-safe", "0",
    "-i", str(concat_list_file),
    "-c", "copy",
    str(output_file)
]

print("🔗 Concatenating segments to output file...")
subprocess.run(concat_cmd, check=True)

# Cleanup
print("🧹 Cleaning up temporary files...")
if concat_list_file.exists():
    concat_list_file.unlink()
for ts_file in ts_files:
    if ts_file and ts_file.exists():
        ts_file.unlink()
if temp_dir.exists():
    temp_dir.rmdir()

print(f"✨ Success! Output file created: {output_file}")
